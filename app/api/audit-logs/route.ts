export async function GET(request: NextRequest) {
  try {
    const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL;
    const supabaseKey = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY;

    const allLogs: any[] = [];

    for (const [bankKey, config] of Object.entries(BANKS)) {
      const edgeFunctionUrl = `${supabaseUrl}/functions/v1/multi-bank-audit-logs`;

      const response = await fetch(edgeFunctionUrl, {
        method: 'POST',
        headers: {
          'Authorization': `Bearer ${supabaseKey}`,
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({ bankKey })
      });

      if (response.ok) {
        const data = await response.json();
        if (data.logs) {
          allLogs.push(...data.logs);
        }
      } else {
        console.error(`Failed to fetch audit logs from ${config.name}`);
      }
    }

    allLogs.sort((a, b) => new Date(b.timestamp).getTime() - new Date(a.timestamp).getTime());

    return NextResponse.json(allLogs);
  } catch (error) {
    console.error('Error fetching audit logs:', error);
    return NextResponse.json({ error: 'Failed to fetch audit logs' }, { status: 500 });
  }
}
